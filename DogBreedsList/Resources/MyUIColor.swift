import UIKit

extension UIColor {
    class func mWhite() -> UIColor {
        return UIColor(red: 244.0/255.0, green: 248.0/255.0, blue: 254.0/255.0, alpha: 1.0)
    }
    class func mLightGray() -> UIColor {
        return UIColor(red: 193.0/255.0, green: 203.0/255.0, blue: 217.0/255.0, alpha: 1.0)
    }
    class func mDarkBlue() -> UIColor {
        return UIColor(red: 70.0/255.0, green: 101.0/255.0, blue: 249.0/255.0, alpha: 1.0)
    }
    class func mLightBlue() -> UIColor {
        return UIColor(red: 67.0/255.0, green: 191.0/255.0, blue: 233.0/255.0, alpha: 1.0)
    }
    class func mPink() -> UIColor {
        return UIColor(red: 233.0/255.0, green: 128.0/255.0, blue: 177.0/255.0, alpha: 1.0)
    }
}

